    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String msg="";
        boolean ok = comprobarCampos();
        //bindeamos la lista de paises en el formulario
        //implementamos en ListaJugador el metodo altaJugador()
        if (ok){
            if(modo.equals("Alta")){
                //en Persona agregamos el equals por Nombre pq 2 personas seran iguales si tienen mismo nombre
                if (SeleccionMar.todosJugadores.existeJugador(elJugador)){
                    JOptionPane.showMessageDialog(this, "Ya existe un jugador con ese nombre", "ERROR: Jugador duplicado", 
                            JOptionPane.ERROR_MESSAGE);
                    ok = false;
                } else {
                    SeleccionMar.todosJugadores.altaJugador(elJugador);
                    msg ="Jugador dado de alta.";
                }
            } else {
                msg = "Jugador modificado.";
            }
            if (ok){
                SeleccionMar.ficheroJugadores.grabar(SeleccionMar.todosJugadores);
                JOptionPane.showMessageDialog(this, msg);
                dispose();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

