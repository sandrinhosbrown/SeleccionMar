    private boolean comprobarCampos(){
        if(jTextField1.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Debes indicar un nombre", "ERROR: Nombre incorrecto", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (jComboBox1.getSelectedIndex() == 0|| elJugador.getNacionalidad().equals("")){
        JOptionPane.showMessageDialog(this, "Debes inficar un pais", "ERROR: Nacionalidad incorrecta", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(jXDatePicker1.getDate() == null){
            JOptionPane.showMessageDialog(this, "Debes indicar una fecha de nacimiento", "ERROR: Fecha de nacimiento vacía", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (jComboBox3.getSelectedIndex() == 0 || elJugador.getPosicion().equals("")){
            JOptionPane.showMessageDialog(this, "Debes indicar una posicion", "ERROR: Posicion incorrecta", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    

